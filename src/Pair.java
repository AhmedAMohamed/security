public class Pair {
    private int x;

    public Pair(int x, int y) {
        this.x = x;
        this.y = y;
    }

    private int y;

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
}
